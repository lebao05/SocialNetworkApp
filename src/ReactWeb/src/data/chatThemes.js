export function getChatTheme(themeId) {
  if (!themeId || themeId === "default") {
    return CHAT_THEMES[0];
  }
  return CHAT_THEMES.find((t) => t.id === themeId) || CHAT_THEMES[0];
}

export function getThemeAccentColor(themeId) {
  const theme = getChatTheme(themeId);
  return theme.bubbleSelf;
}
